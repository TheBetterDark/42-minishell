#include "../inc/data.h"

void	skyy_pwd()
{
	char	buf[256];

	if (getcwd(buf, sizeof(buf)) == NULL)
	{
		perror("cwd");
		exit(EXIT_FAILURE);
	}
	ft_putendl_fd(buf, 1);
}
